def parse_args(argv: list[str] | None = None) -> argparse.Namespace:
    if argv is None:
        argv = sys.argv[1:]

    pre = argparse.ArgumentParser(add_help=False)
    pre.add_argument("--config", default=DEFAULT_CONFIG_FILE)
    pre_args, _ = pre.parse_known_args(argv)
    explicit_config = any(token == "--config" or token.startswith("--config=") for token in argv)

    config = load_toml_config(pre_args.config)
    defaults = pick_config_defaults(config)
    parser = build_parser(defaults)
    args = parser.parse_args(argv)
    args.config = pre_args.config
    args._explicit_config = explicit_config  # type: ignore[attr-defined]
    return args


def resolve_urls_with_passwords(args: argparse.Namespace) -> list[tuple[str, str | None]]:
    if args.url:
        return [(args.url, None)]

    urls_file = args.urls_file or DEFAULT_URLS_FILE
    path = Path(urls_file)
    if not path.is_absolute():
        path = Path.cwd() / path

    if not path.exists():
        raise FileNotFoundError(
            f"未找到输入文件：{path}\n"
            "请使用 --url 指定单个链接，或使用 --urls-file 指定 txt 文件，"
            f"也可以在当前目录放置默认的 {DEFAULT_URLS_FILE}。"
        )

    return read_urls_file_with_passwords(str(path))


async def run_one(url: str, password: str | None, out_dir: str, args: argparse.Namespace) -> None:
    prov = args.provider if args.provider != "auto" else detect_provider(url)

    if prov == "tz":
        await run_tz_one(url, out_dir, args.mode, args.headless)
    elif prov == "fz":
        await run_fz_one(url, out_dir, args.mode, args.headless, args, password)
    elif prov == "nyfy":
        await run_nyfy_one(url, out_dir, args.headless, args.out_parent, args)
    elif prov == "cloud":
        run_cloud_one(url, out_dir, args)
    else:
        await run_fz_one(url, out_dir, args.mode, args.headless, args, password)


async def run_download(args: argparse.Namespace, urls_with_password: list[tuple[str, str | None]] | None = None) -> None:
    if args.save_config:
        write_config_file(args.config, build_config_payload(args))
        print(f">>> 已保存配置：{os.path.abspath(args.config)}")
        return

    generated = ensure_starter_config_file(args.config, getattr(args, "_explicit_config", False))
    if generated:
        print(f">>> 已生成默认配置模板：{os.path.abspath(args.config)}")

    if urls_with_password is None:
        urls_with_password = resolve_urls_with_passwords(args)

    out_parent = os.path.abspath(args.out_parent)
    os.makedirs(out_parent, exist_ok=True)

    print("\n>>> 启动参数：")
    print(f"    config      : {args.config}")
    print(f"    URL数量     : {len(urls_with_password)}")
    print(f"    out_parent  : {out_parent}")
    print(f"    provider    : {args.provider}")
    print(f"    mode        : {args.mode}")
    print(f"    headless    : {args.headless}")
    print(f"    max_rounds  : {args.max_rounds}")
    print(f"    step_wait   : {args.step_wait_ms}ms\n")

    for i, (url, password) in enumerate(urls_with_password, start=1):
        share_id = extract_share_id(url)
        out_dir = os.path.join(out_parent, share_id)
        os.makedirs(out_dir, exist_ok=True)

        prov = args.provider if args.provider != "auto" else detect_provider(url)
        print("=" * 80)
        print(f"### [{i}/{len(urls_with_password)}] provider={prov}")
        print(f"URL      : {url}")
        if password:
            print(f"安全码   : {'*' * len(password)}")
        print(f"输出目录 : {out_dir}")
        print("=" * 80)

        try:
            await run_one(url, password, out_dir, args)
        except Exception as e:
            print(f">>> ❌ 失败：{url}")
            print(f">>> 错误：{e}")
            continue

        if not args.no_zip:
            zip_path = os.path.join(out_parent, f"{share_id}.zip")
            make_zip_dir(out_dir, zip_path)
            print(f">>> zip 已生成：{os.path.abspath(zip_path)}")

    print("\n>>> 全部任务结束")


async def main_async(argv: list[str] | None = None) -> None:
    args = parse_args(argv)
    await run_download(args)


# ================== 菜单模式 ==================


def select_menu_action() -> str:
    while True:
        show_menu()
        choice = input("请选择功能 [7]: ").strip() or "7"
        if choice in {"1", "2", "3", "4", "5", "6", "7"}:
            return choice
        print("    请输入 1-7。")


def interactive_main() -> None:
    config_path, state, generated = load_effective_state(DEFAULT_CONFIG_FILE)
    if generated:
        print(f">>> 已生成默认配置模板：{os.path.abspath(config_path)}")

    choice = select_menu_action()

    if choice == "1":
        url = prompt_text("请输入单个检查链接 URL")
        password = prompt_text("安全码（可选，直接回车跳过）", "") or None
        updated = collect_menu_settings(state)
        args = build_runtime_namespace(updated, url=url)
        asyncio.run(run_download(args, urls_with_password=[(url, password)]))
        return

    if choice == "2":
        path = prompt_text("请输入 txt 文件路径", DEFAULT_URLS_FILE)
        updated = collect_menu_settings(state)
        args = build_runtime_namespace(updated, urls_file=path)
        asyncio.run(run_download(args))
        return

    if choice == "3":
        updated = collect_menu_settings(state)
        args = build_runtime_namespace(updated, urls_file=DEFAULT_URLS_FILE)
        asyncio.run(run_download(args))
        return

    if choice == "4":
        show_config(config_path, state)
        return

    if choice == "5":
        updated = collect_menu_settings(state)
        write_config_file(config_path, updated)
        print(f">>> 已保存配置：{os.path.abspath(config_path)}")
        return

    if choice == "6":
        write_config_file(config_path, state)
        print(f">>> 已保存配置：{os.path.abspath(config_path)}")
        return

    print(">>> 已退出")


# ================== 命令行入口 ==================


def main() -> None:
    if len(sys.argv) > 1:
        asyncio.run(main_async())
        return
    interactive_main()


if __name__ == "__main__":
    main()
