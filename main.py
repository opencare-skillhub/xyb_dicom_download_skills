# ================== 配置与序列化 ==================


def load_toml_config(config_path: str | None) -> dict[str, Any]:
    if not config_path:
        return {}

    path = Path(config_path)
    if not path.exists():
        return {}

    with path.open("rb") as f:
        data = tomllib.load(f)
    return data if isinstance(data, dict) else {}


def pick_config_defaults(config: dict[str, Any]) -> dict[str, Any]:
    defaults = dict(DEFAULTS)
    for key in defaults:
        if key in config and config[key] is not None:
            defaults[key] = config[key]
    return defaults


def _toml_escape(value: str) -> str:
    return json.dumps(value, ensure_ascii=False)


def dump_toml_config(data: dict[str, Any]) -> str:
    lines: list[str] = ["# Auto-generated by main.py"]
    for key in SAVED_CONFIG_KEYS:
        value = data.get(key)
        if value is None:
            continue
        if isinstance(value, bool):
            rendered = "true" if value else "false"
        elif isinstance(value, (int, float)):
            rendered = str(value)
        else:
            rendered = _toml_escape(str(value))
        lines.append(f"{key} = {rendered}")
    lines.append("")
    return "\n".join(lines)


def build_config_payload(args: argparse.Namespace) -> dict[str, Any]:
    payload: dict[str, Any] = {}
    for key in SAVED_CONFIG_KEYS:
        payload[key] = getattr(args, key, DEFAULTS.get(key))
    return payload


def write_config_file(path: str | Path, data: dict[str, Any]) -> Path:
    target = Path(path)
    if target.parent and str(target.parent) not in {"", "."}:
        target.parent.mkdir(parents=True, exist_ok=True)
    target.write_text(dump_toml_config(data), encoding="utf-8")
    return target


def ensure_starter_config_file(config_path: str, explicit_config: bool) -> bool:
    """Create a starter config on first run when using the default path."""
    if explicit_config:
        return False
    if config_path != DEFAULT_CONFIG_FILE:
        return False

    target = Path(config_path)
    if target.exists():
        return False

    write_config_file(target, dict(DEFAULTS))
    return True


def load_effective_state(config_path: str = DEFAULT_CONFIG_FILE) -> tuple[str, dict[str, Any], bool]:
    generated = ensure_starter_config_file(config_path, explicit_config=False)
    state = pick_config_defaults(load_toml_config(config_path))
    return config_path, state, generated


# ================== 终端交互 ==================


def prompt_text(label: str, default: str | None = None) -> str:
    suffix = f" [{default}]" if default not in {None, ""} else ""
    value = input(f"{label}{suffix}: ").strip()
    if value == "" and default is not None:
        return str(default)
    return value


def prompt_yes_no(label: str, default: bool = False) -> bool:
    suffix = "Y/n" if default else "y/N"
    value = input(f"{label} ({suffix}): ").strip().lower()
    if value == "":
        return default
    return value in {"y", "yes", "1", "true", "t"}


def prompt_int(label: str, default: int) -> int:
    while True:
        value = input(f"{label} [{default}]: ").strip()
        if value == "":
            return default
        try:
            return int(value)
        except ValueError:
            print("    请输入数字。")


