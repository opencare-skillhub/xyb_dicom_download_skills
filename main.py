    ap.add_argument("--step-wait-ms", type=int, default=defaults["step_wait_ms"], help="逐帧间隔（毫秒）（复肿 fz）")
    ap.add_argument("--quiet-checks", type=int, default=defaults["quiet_checks"], help="静默观察次数（复肿 fz）")
